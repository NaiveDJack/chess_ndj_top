      print('│ ')
      @squares.each_value do |column|
        print(column[:"r#{row}"], '  │ ')
      end
      row == 1 ? puts(' ', '└───┴───┴───┴───┴───┴───┴───┴───┘') : puts(' ', '├───┼───┼───┼───┼───┼───┼───┼───┤')
